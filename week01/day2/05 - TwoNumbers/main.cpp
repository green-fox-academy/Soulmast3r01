#include <iostream>

int main() {
    int a = 13;
    int b = 22;

    std::cout << a + b << std::endl;

    std::cout << a - b << std::endl;

    std::cout << a * b << std::endl;

    std::cout << (float)b / a << std::endl;

    std::cout << b / a << std::endl;

    std::cout << b % a << std::endl;


    return 0;
}